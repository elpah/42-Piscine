int ft_is_prime(int nb)
{
    int i;

    i = 2;
    if (nb < 2)
    {
        return (0);
    }
    if (nb == 2)
    {
        return (1);
    }
    while ((2147483647 > nb) && i < nb)
    {
        if (nb % i == 0)
        {
            return (0);
        }
        i++;
    }
    return (1);
}
